#include "../include/libft.h"

void	ft_printbit(const char o)
{
	int	a;
	int	b;

	a = 0;
	b = 128;
	while (a < 8)
	{
		if (o & b)
			ft_putchar('1');
		else
			ft_putchar('0');
		b = b >> 1;
		a++;
	}
}
