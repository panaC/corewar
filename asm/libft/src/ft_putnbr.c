#include "../include/libft.h"

void		ft_putnbr(int n)
{
	long	b;

	b = n;
	if (b < 0)
	{
		b = -b;
		ft_putchar('-');
	}
	if (b >= 10)
	{
		ft_putnbr(b / 10);
		ft_putchar(b % 10 + 48);
	}
	else
		ft_putchar(b + 48);
}
